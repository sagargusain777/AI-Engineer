
#Python List  are built-in data structures that can store multiple items in a single variable.
#List can contain duplicate items 
#List are mutable, meaning you can change their content without changing their identity.

#Creating a List
my_list = [1, 2, 3, 4, 5]
print("Original List:", my_list)
fruits = ["apple", "banana", "cherry"]
print("Fruits List:", fruits)
mixed_list = [1, "hello", 3.14, True]
print("Mixed List:", mixed_list)
#Accessing List Elements
print("First Fruit in the Basket:", fruits[0])  # Output: apple
print("Last Fruit in the Basket:", fruits[-1])  # Output: cherry
print ("Slicing Fruits List:", fruits[1:3])  # Output: ['banana', 'cherry']


